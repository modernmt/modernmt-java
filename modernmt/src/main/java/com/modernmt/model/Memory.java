package com.modernmt.model;

public class Memory extends Model {

    private long id;
    private String name;
    private String description;
    private String creationDate;

    public long getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public String getDescription() {
        return description;
    }

    public String getCreationDate() {
        return creationDate;
    }

}
